class Gallery extends Component {
  state = {
    activeImg: imagesList[0].imageUrl,
    activeImgAlt: imagesList[0].imageAltText,
  }

  getNewImgOf = imageUrl => {
    const filterNewImg = imagesList.filter(
      eachImgItem => eachImgItem.imageUrl === imageUrl,
    )
    this.setState({
      activeImg: filterNewImg[0].imageUrl,
      activeImgAlt: filterNewImg[0].imageAltText,
    })
  }

  render() {
    const {activeImg, activeImgAlt} = this.state

    return (
      <div className="bg-container">
        <div className="inner-container">
          <img src={activeImg} alt={activeImgAlt} className="imgSize" />
          <h1 className="heading">Nature Photography</h1>
          <p className="paragraph">Nature Photography by Rahul</p>

          <ul className="listOfImg">
            {imagesList.map(eachImage => (
              <ThumbnailItem
                key={eachImage.id}
                thumbNailImgUrl={eachImage}
                getNewImg={this.getNewImgOf}
                isActive={eachImage.imageUrl === activeImg}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}

export default Gallery
